                             Ia menyebutkan, bahwa fenomena lain adalah para diabetesi mengalami kesulitan untuk menemukan makanan rendah glukosa yang aman dikonsumsi sehari-hari, seperti toko-toko roti dan swalayan. Karena itu, buku “Makan Enak Meskipun Diabetes: Menu Sehat Harian Bagi Para Diabetesi” dinilai sangat layak untuk dimiliki oleh siapa saja, yang ingin hidup lebih sehat. Terutama bagi anda yang ingin mencegah diabetes sejak dini, dan atau bagi seorang diabetesi untuk mengurangi komplikasi diabetes lebih lanjut.
