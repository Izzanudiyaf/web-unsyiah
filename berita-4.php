                        <p class="lead color-black text-justify" style="line-height: 30px;">
                             Dosen Fakultas Keperawatan Universitas Syiah Kuala (FKep USK), Ns. Nani Safuni, MNg, resmi merilis buku berjudul “Makan Enak Meskipun Diabetes: Menu Sehat Harian Bagi Para Diabetesi”. Karya tersebut diterbitkan oleh Deepublishing.
                            <br><br>
                            Buku ini mengulas tentang pengolahan makanan yang ramah bagi orang yang didiagnosa diabetes mellitus atau disebut dengan istilah diabetesi. Buku ini berisikan total 65 resep, mulai dari 11 resep menu utama,27 resep lauk dan sayur, 22 resep cemilan dan 5 resep minuman dengan menggunakan bahan lokal, nasional dan western. Pada setiap menu ditampilkan evidence based practice dan manfaat bahan utama dalam menu tersebut. 
                            <br><br>
                            “Buku ini hadir sebagai rekomendasi pemilihan makanan bagi keluarga yang hidup dengan diabetesi. Tak bisa kita pungkiri, prevalensi diabetes mellitus yang kecenderungan semakin meningkat angkanya setiap tahun baik di Indonesia maupun di dunia,” katanya.
